package inheritance3;

public class HotelRoom {
	protected  String hotelname;

	public HotelRoom(String hotelname) {
		super();
		this.hotelname = hotelname;
	}

	public String getHotelname() {
		return hotelname;
	}

	public void setHotelname(String hotelname) {
		this.hotelname = hotelname;
	}
	

}

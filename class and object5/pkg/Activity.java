
public class Activity {
	private byte ln;
	private byte tm;
	private String fp;
	private float ft;
	public byte getLn() {
		return ln;
	}
	public void setLn(byte ln) {
		this.ln = ln;
	}
	public byte getTm() {
		return tm;
	}
	public void setTm(byte tm) {
		this.tm = tm;
	}
	public String getFp() {
		return fp;
	}
	public void setFp(String fp) {
		this.fp = fp;
	}
	public float getFt() {
		return ft;
	}
	public void setFt(float ft) {
		this.ft = ft;
	}

}
